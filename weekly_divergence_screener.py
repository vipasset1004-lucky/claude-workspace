def get_krx_tickers(market="ALL", top_n=300):
    """한국 주식 종목 코드 수집 (KRX 공시 + yfinance 시가총액 정렬)"""
    try:
        # 1단계: KRX에서 전체 종목 리스트 가져오기
        print("  KRX 종목 리스트 다운로드 중...")
        url = "http://kind.krx.co.kr/corpgeneral/corpList.do?method=download&searchType=13"
        tables = pd.read_html(url, encoding="euc-kr")
        df = tables[0]

        # 종목코드 6자리 포맷 + 순수 숫자만 (채권/펀드 제외)
        df["종목코드"] = df["종목코드"].astype(str).str.zfill(6)
        df = df[df["종목코드"].str.match(r"^\d{6}$")]
        df = df[~df["종목코드"].str.contains("[A-Za-z]")]

        print(f"  전체 종목: {len(df)}개")

        # 2단계: yfinance로 시가총액 기준 상위 종목 추출
        print("  시가총액 기준 정렬 중...")
        cap_data = []
        checked = 0

        for _, row in df.iterrows():
            ticker = row["종목코드"]
            name = row["회사명"]

            # yfinance에서 시가총액 확인
            for suffix in [".KS", ".KQ"]:
                try:
                    info = yf.Ticker(f"{ticker}{suffix}")
                    hist = info.history(period="5d")
                    if hist.empty:
                        continue

                    # 시가총액 추정 (최근 종가 * 발행주식수는 비용이 크므로, 거래대금으로 대체)
                    avg_volume = hist["Volume"].mean()
                    last_price = hist["Close"].iloc[-1]
                    trade_value = avg_volume * last_price  # 일평균 거래대금

                    mkt = "KOSPI" if suffix == ".KS" else "KOSDAQ"
                    cap_data.append({
                        "ticker": ticker,
                        "name": name,
                        "suffix": suffix,
                        "market": mkt,
                        "trade_value": trade_value,
                        "price": last_price
                    })
                    break
                except:
                    continue

            checked += 1
            if checked % 50 == 0:
                print(f"  확인: {checked}개... (유효: {len(cap_data)}개)")

            # 충분한 후보를 모으면 중단 (top_n의 2배)
            if len(cap_data) >= top_n * 2:
                break

            time.sleep(0.15)

        # 거래대금 기준 내림차순 정렬 (활발한 종목 우선)
        cap_data.sort(key=lambda x: x["trade_value"], reverse=True)

        # 시장 필터
        if market == "KOSPI":
            cap_data = [x for x in cap_data if x["market"] == "KOSPI"]
        elif market == "KOSDAQ":
            cap_data = [x for x in cap_data if x["market"] == "KOSDAQ"]

        result = [{"ticker": x["ticker"], "name": x["name"]} for x in cap_data[:top_n]]
        print(f"  → 최종 {len(result)}개 종목 선정 완료")
        return result

    except Exception as e:
        print(f"[ERROR] KRX 종목 수집 실패: {e}")
        print("  → 주요 종목 기본 리스트로 대체합니다.")
        return get_fallback_tickers(top_n)


def get_new_listings(existing_tickers, cache_path="new_listings_cache.json", cache_days=7,
                    min_weeks=52, max_weeks=104, max_candidates=80, force_refresh=False):
    """신규 상장 종목(주봉 52-104주) 수집 — KRX 상장일 기반 + 캐시
    existing_tickers: 기존 유니버스(fallback)에 있는 ticker set/list (제외용)
    반환: [{"ticker","name","themes":["신규상장"],"is_new_listing":True,"listing_date":"YYYY-MM-DD","weeks_available":N}]
    """
    # 1) 캐시 확인 (구버전 themes:["신규상장"] 이면 강제 갱신)
    if not force_refresh:
        try:
            if os.path.exists(cache_path):
                with open(cache_path, "r", encoding="utf-8") as f:
                    cached = json.load(f)
                updated = datetime.fromisoformat(cached.get("updated_at", "1970-01-01"))
                listings = cached.get("listings", [])
                # 구버전 감지: themes가 ["신규상장"] 단독인 항목이 있으면 갱신
                is_old_format = any(l.get("themes") == ["신규상장"] for l in listings[:3])
                if not is_old_format and (datetime.now() - updated).days < cache_days:
                    print(f"  [cache] 신규상장 {len(listings)}개 로드 ({updated.strftime('%Y-%m-%d')})")
                    return listings
                if is_old_format:
                    print("  [cache] 신규상장 구버전 캐시 감지 → 재생성")
        except Exception as e:
            print(f"  [cache] 신규상장 캐시 읽기 실패: {e}")

    # 2) KRX corp list 수집
    try:
        import io
        print("  신규상장 스캔: KRX corp list 다운로드 중...")
        url = "http://kind.krx.co.kr/corpgeneral/corpList.do?method=download&searchType=13"
        r = requests.get(url, timeout=15)
        r.encoding = "euc-kr"
        tables = pd.read_html(io.StringIO(r.text))
        df = tables[0]
        df["종목코드"] = df["종목코드"].astype(str).str.zfill(6)
        df = df[df["종목코드"].str.match(r"^\d{6}$")]
        df["상장일"] = pd.to_datetime(df["상장일"], errors="coerce")

        # 상장일 ≤ 2년전 & ≥ 12개월전 → 주봉 52-104주 가용 구간
        today = pd.Timestamp.now().normalize()
        upper = today - pd.Timedelta(weeks=min_weeks)        # 최근 상장 제외 (52주 미만)
        lower = today - pd.Timedelta(weeks=max_weeks + 8)    # 여유 8주 버퍼
        recent = df[(df["상장일"] >= lower) & (df["상장일"] <= upper)].copy()

        existing_set = set(existing_tickers)
        recent = recent[~recent["종목코드"].isin(existing_set)]
        # 스팩 제외 (합병 목적 페이퍼컴퍼니, 기술적 분석 의미 없음)
        recent = recent[~recent["회사명"].str.contains("스팩", na=False)]
        recent = recent.sort_values("상장일", ascending=False).head(max_candidates)
        print(f"  신규상장 후보: {len(recent)}개 (상장일 {lower.date()} ~ {upper.date()})")

        # 업종 → 짧은 테마명 변환 (인코딩 깨진 문자 제거)
        def parse_sector(업종_str):
            if not isinstance(업종_str, str) or not 업종_str.strip():
                return []
            # 인코딩 깨진 문자 제거 (서로게이트 등)
            s = 업종_str.encode("utf-8", errors="ignore").decode("utf-8").strip()
            if not s:
                return []
            for suffix in ["제조업", "판매업", "서비스업", "공급업", "개발업", "운영업", "임대업", "관련업", "업"]:
                s = s.replace(suffix, "").strip()
            s = s.replace("및", "").replace("의", "").strip()
            parts = [p.strip() for p in s.split() if len(p.strip()) >= 2]
            return [parts[0]] if parts else []

        # 3) 각 후보에 대해 주봉 데이터 확인
        listings = []
        for idx, row in recent.iterrows():
            ticker = row["종목코드"]
            name = row["회사명"]
            listing_date = row["상장일"].strftime("%Y-%m-%d") if pd.notna(row["상장일"]) else None
            sector_themes = parse_sector(row.get("업종", ""))
            data = fetch_weekly_data(ticker, is_korean=True, weeks=max_weeks)
            if data is None:
                continue
            n = len(data)
            if min_weeks <= n <= max_weeks:
                listings.append({
                    "ticker": ticker,
                    "name": name,
                    "themes": sector_themes,
                    "is_new_listing": True,
                    "listing_date": listing_date,
                    "weeks_available": n,
                })
            time.sleep(0.1)

        print(f"  신규상장 확정: {len(listings)}개")

        # 4) 캐시 저장
        try:
            with open(cache_path, "w", encoding="utf-8") as f:
                json.dump({"updated_at": datetime.now().isoformat(), "listings": listings},
                          f, ensure_ascii=False, indent=2)
        except Exception as e:
            print(f"  [cache] 신규상장 캐시 저장 실패: {e}")

        return listings
    except Exception as e:
        print(f"[ERROR] 신규상장 수집 실패: {e}")
        return []


