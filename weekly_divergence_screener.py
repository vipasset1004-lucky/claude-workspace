def fetch_daily_data(ticker_code, is_korean=True, days=120):
    """일봉 데이터 수집 (최근 120일 - 단기 타이밍용)"""
    try:
        if is_korean:
            symbol = f"{ticker_code}.KS"
            data = yf.download(symbol, period=f"{days}d", interval="1d",
                               progress=False, timeout=10)
            if data.empty:
                symbol = f"{ticker_code}.KQ"
                data = yf.download(symbol, period=f"{days}d", interval="1d",
                                   progress=False, timeout=10)
        else:
            data = yf.download(ticker_code, period=f"{days}d", interval="1d",
                               progress=False, timeout=10)

        if data.empty:
            return None

        if isinstance(data.columns, pd.MultiIndex):
            data.columns = data.columns.get_level_values(0)

        # NaN 행 제거 (yfinance가 가끔 최신 행을 NaN으로 반환)
        data = data.dropna(subset=["Close"])
        if data.empty:
            return None

        return data
    except:
        return None


def score_daily(df):
    """일봉 타이밍 점수 (10점 만점) - 지금 진입할 타이밍인가?"""
    if df is None or len(df) < 20:
        return None

    close = df["Close"].values
    high = df["High"].values
    low = df["Low"].values
    volume = df["Volume"].values
    n = len(df)
    score = 0
    signals = []

    # 이동평균
    ma5 = pd.Series(close).rolling(5).mean().values
    ma20 = pd.Series(close).rolling(20).mean().values
    ma60 = pd.Series(close).rolling(60).mean().values if n >= 60 else None

    # RSI 계산 (간이)
    deltas = np.diff(close)
    gains = np.where(deltas > 0, deltas, 0)
    losses = np.where(deltas < 0, -deltas, 0)
    avg_gain = pd.Series(gains).rolling(14).mean().values
    avg_loss = pd.Series(losses).rolling(14).mean().values
    rs = np.where(avg_loss > 0, avg_gain / avg_loss, 0)
    rsi = 100 - (100 / (1 + rs))
    current_rsi = float(rsi[-1]) if len(rsi) > 0 and not np.isnan(rsi[-1]) else 50

    # ── [1] 거래량 폭발 (20점) ← 가장 중요한 일봉 신호
